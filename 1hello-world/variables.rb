=begin
Untuk membuat variable pada Ruby, kita tidak memerlukan syntax apapun,
kita hanya perlu membuat nama variable yang kita inginkan
=end

myVariable = 'ini adalah variable yang saya buat'

puts "saya akan memanggil #{myVariable}"
#Output: saya akan memanggil ini adalah variable yang saya buat

=begin
cara saya diatas #{myVariable} yaitu sebuah syntax untuk memanggil variable yang telah kita buat
=end

firstName = "memet"
age = 23

puts "my name is #{firstName} and I'm #{age} years old."
#output: my name is memet and I'm 23 years old.
